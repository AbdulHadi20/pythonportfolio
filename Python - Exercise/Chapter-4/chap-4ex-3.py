# a program to demonstrate the working of if-elif-else statements

# in this section, the first condition is true
alien_color = 'blue'

if alien_color == 'blue':
    print('You have earned 5 points.')

elif alien_color == 'white':
    print('You have earned 10 points.')

else:
    print('You have earned 15 points.')

# in this section, the second condition is true
alien_color = 'white'

if alien_color == 'blue':
    print('You have earned 5 points.')

elif alien_color == 'white':
    print('You have earned 10 points.')

else:
    print('You have earned 15 points.')

# in this section, the last condition is true
alien_color = 'brown'

if alien_color == 'blue':
    print('You have earned 5 points.')

elif alien_color == 'white':
    print('You have earned 10 points.')

else:
    print('You have earned 15 points.')
